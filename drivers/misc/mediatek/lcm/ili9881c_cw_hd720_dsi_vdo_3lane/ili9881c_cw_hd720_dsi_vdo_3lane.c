static void lcm_update(unsigned int x, unsigned int y,
                       unsigned int width, unsigned int height)
{
	unsigned int x0 = x;
	unsigned int y0 = y;
	unsigned int x1 = x0 + width - 1;
	unsigned int y1 = y0 + height - 1;

	unsigned char x0_MSB = ((x0>>8)&0xFF);
	unsigned char x0_LSB = (x0&0xFF);
	unsigned char x1_MSB = ((x1>>8)&0xFF);
	unsigned char x1_LSB = (x1&0xFF);
	unsigned char y0_MSB = ((y0>>8)&0xFF);
	unsigned char y0_LSB = (y0&0xFF);
	unsigned char y1_MSB = ((y1>>8)&0xFF);
	unsigned char y1_LSB = (y1&0xFF);

	unsigned int data_array[16];

	data_array[0]= 0x00053902;
	data_array[1]= (x1_MSB<<24)|(x0_LSB<<16)|(x0_MSB<<8)|0x2a;
	data_array[2]= (x1_LSB);
	data_array[3]= 0x00053902;
	data_array[4]= (y1_MSB<<24)|(y0_LSB<<16)|(y0_MSB<<8)|0x2b;
	data_array[5]= (y1_LSB);
	data_array[6]= 0x002c3909;

	dsi_set_cmdq(&data_array, 7, 0);

}
#endif

static unsigned int lcm_compare_id(void)
{
	unsigned int id1 = 0,id2 = 0,id3 = 0;
	unsigned int lcm_id = 0;
	unsigned char buffer[3];
	unsigned int array[8];

	SET_RESET_PIN(1);
	MDELAY(1);
	SET_RESET_PIN(0);
	MDELAY(10);
	SET_RESET_PIN(1);
	MDELAY(100);

	array[0] = 0x00043902;
	array[1] = 0x018198FF;
	dsi_set_cmdq(array, 2, 1);
	MDELAY(1);
	
	array[0] = 0x00013700;// return byte number
	dsi_set_cmdq(array, 1, 1);
	MDELAY(1);

	read_reg_v2(0x00, buffer, 1);
	id1 = buffer[0];	

	read_reg_v2(0x01, buffer, 1);
	id2 = buffer[0];

	read_reg_v2(0x02, buffer, 1);
	id3 = buffer[0];

	lcm_id = ((id1<<8) | id2);

#ifdef BUILD_LK
	printf("LK,ILI9881C: id1=0x%x,id2=0x%x,id3=0x%x\n",id1,id2,id3);   
#else
	printk("KERNEL,ILI9881C: id1=0x%x,id2=0x%x,id3=0x%x\n",id1,id2,id3);  
#endif

	if ((id1==0x98)&&(id2==0x81)&&(id3==0x0C))
		return 1;
	else
		return 0;

	//return (ILI9881C_LCM_ID == lcm_id)?1:0;
}


